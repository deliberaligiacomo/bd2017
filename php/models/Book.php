<?php

    /**
     * A Book
     */
    class Book {

        /**
         * The book identifier
         * @var type number
         */
        public $id;

        /**
         * The book title
         * @var type string
         */
        public $title;

        /**
         * The book image url
         * @var type string
         */
        public $image;

        /**
         * The book genre
         * @var type string
         */
        public $genre;

        /**
         * The book rating (number of stars)
         * @var type float
         */
        public $rating;

        /**
         * The autho's book full name
         * @var type string
         */
        public $author;

    }

?>